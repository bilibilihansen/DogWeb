let currentLang = localStorage.getItem('siteLang') || 'zh';

document.addEventListener('DOMContentLoaded', function() {
    initLanguageSwitcher();
});

function initLanguageSwitcher() {
    const langOptions = document.querySelectorAll('.lang-option');
    
    updateLanguage(currentLang);
    
    langOptions.forEach(option => {
        option.addEventListener('click', handleLanguageSelect);
    });
}

function handleLanguageSelect(event) {
    const lang = this.getAttribute('data-lang');
    changeLanguage(lang);
}

function changeLanguage(lang) {
    if (lang === currentLang) return;

    currentLang = lang;
    
    localStorage.setItem('siteLang', lang);
    
    updateLanguage(lang);
}

function updateLanguage(lang) {
    updateLanguageButton(lang);
    
    updateTranslatableElements(lang);
    
    updateLanguageOptionState(lang);
    
    document.documentElement.setAttribute('lang', lang === 'zh' ? 'zh-CN' : 'en-US');
}

function updateLanguageButton(lang) {
    const langToggle = document.getElementById('langToggle');
    if (!langToggle) return;
    
    const langFlag = langToggle.querySelector('.lang-flag');
    const langText = langToggle.querySelector('.lang-text');
    
    if (lang === 'zh') {
        langFlag.textContent = '🇨🇳';
        langText.textContent = translations[lang]['lang.zh'];
        langToggle.setAttribute('data-current-lang', 'zh');
    } else {
        langFlag.textContent = '🇺🇸';
        langText.textContent = translations[lang]['lang.en'];
        langToggle.setAttribute('data-current-lang', 'en');
    }
}

function updateTranslatableElements(lang) {
    const langTranslations = translations[lang];
    if (!langTranslations) {
        console.warn(`找不到语言 "${lang}" 的翻译数据`);
        return;
    }

    const translatableElements = document.querySelectorAll('[data-i18n]');
    
    translatableElements.forEach(element => {
        const translationKey = element.getAttribute('data-i18n');
        
        if (langTranslations[translationKey]) {
            element.textContent = langTranslations[translationKey];
        } 
        else {
            console.warn(`未找到翻译键 "${translationKey}" 的语言 "${lang}" 翻译`);
        }
    });
}

function updateLanguageOptionState(lang) {
    const langOptions = document.querySelectorAll('.lang-option');

    langOptions.forEach(option => {
        const optionLang = option.getAttribute('data-lang');
        
        if (optionLang === lang) {
            option.classList.add('active');
            option.setAttribute('aria-current', 'true'); // 辅助功能
        } else {
            option.classList.remove('active');
            option.removeAttribute('aria-current');
        }
    });
}

function getTranslation(key, lang = currentLang) {
    if (translations[lang] && translations[lang][key]) {
        return translations[lang][key];
    }
    
    if (lang !== 'zh' && translations['zh'] && translations['zh'][key]) {
        return translations['zh'][key];
    }
    
    return key;
}

window.LanguageManager = {
    changeLanguage,
    getCurrentLang: () => currentLang,
    getTranslation,
    updateLanguage
};
